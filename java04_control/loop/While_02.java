package java04_control.loop;

public class While_02 {
	public static void main(String[] args) {
		
		int i=1;
		while ( i<=10) {
			System.out.println(i);
			
			i++;
		}
		
		System.out.println("-------------");
		
		int j=1;
		for ( ;i<=10;) {
			System.out.println(i);
			
			j++;
		
		}	
		
	}
}
